        FlatIntelliJLaf.registerCustomDefaultsSource("style");
        FlatIntelliJLaf.setup();

        UIManager.put("Table.showVerticalLines", false); // ẩn đường kể dọc
        UIManager.put("Table.showHorizontalLines", true); // hiện đường kẻ ngang
