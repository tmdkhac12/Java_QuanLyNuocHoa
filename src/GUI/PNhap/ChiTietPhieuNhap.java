    private void loadChiTietPhieuNhap() {
        ChiTietPhieuNhapDAO dao = new ChiTietPhieuNhapDAO();
        List<ChiTietPhieuNhapDTO> chiTietList = dao.getChiTietPhieuNhapByMaPhieu(maPhieu);

        DefaultTableModel model = (DefaultTableModel) tblsanphamchitiet.getModel();
        model.setRowCount(0); // xóa dữ liệu cũ
        int stt = 1;

        for (ChiTietPhieuNhapDTO ct : chiTietList) {
            model.addRow(new Object[]{
                stt++,
                ct.getPerfumeId(),
                ct.getPerfumeName(),
                ct.getSex(),
                ct.getScentType(),
                ct.getBrand(),
                ct.getCost(),
                ct.getQuantity()
            });
        }
    }

    private void initTable() {
        String[] columnNames = {"STT", "Mã nước hoa", "Tên", "Giới tính", "Loại mùi", "Thương hiệu", "Giá", "Số lượng"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        tblsanphamchitiet.setModel(model);
    }

    private void setUpTable() {
        // Set ẩn hiển thị ô vuông khi bấm vào cell 
        tblsanphamchitiet.setFocusable(false);

        // Set không cho cell có thể chỉnh sửa 
        tblsanphamchitiet.setDefaultEditor(Object.class, null);
    }
    

    public void centerTableData() {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);

        TableColumnModel columnModel = tblsanphamchitiet.getColumnModel();
        for (int i = 0; i < columnModel.getColumnCount(); i++) {
            columnModel.getColumn(i).setCellRenderer(centerRenderer);
        }
    }
    

