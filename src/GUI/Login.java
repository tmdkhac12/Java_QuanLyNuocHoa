    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed

        String username = txtTenDangNhap.getText().trim();
        String password = new String(txtMatKhau.getPassword()).trim();

        NhanVienDTO nhanVien = nhanVienBUS.login(username, password);
        if (nhanVien != null) {
            ThemHoaDon.setLoggedInEmployee(nhanVien.getId(), nhanVien.getName());
            this.dispose();
            new Main(nhanVien).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập hoặc mật khẩu không đúng!");
        }


    }//GEN-LAST:event_btnDangNhapActionPerformed

